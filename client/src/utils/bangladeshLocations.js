export const getDivisions = () => Object.keys(bangladeshLocations);

export const getDistricts = (division) => {
  if (!division || !bangladeshLocations[division]) return [];
  return Object.keys(bangladeshLocations[division]);
};

export const getUpazillas = (division, district) => {
  if (!division || !district) return [];
  const div = bangladeshLocations[division];
  if (!div || !div[district]) return [];
  return Object.keys(div[district]);
};

export const getAreas = (division, district, upazilla) => {
  if (!division || !district || !upazilla) return [];
  const div = bangladeshLocations[division];
  if (!div) return [];
  const dist = div[district];
  if (!dist) return [];
  return dist[upazilla] || [];
};